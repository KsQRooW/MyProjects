"""
Статья 83 закона “О выборах депутатов Государственной Думы Федерального Собрания Российской Федерации”
определяет следующий алгоритм пропорционального распределения мест в парламенте.

Необходимо распределить 450 мест между партиями, участвовавших в выборах.
Сначала подсчитывается сумма голосов избирателей, поданных за каждую партию и подсчитывается сумма голосов,
поданных за все партии.
Эта сумма делится на 450, получается величина, называемая “первое избирательное частное”
(смысл первого избирательного частного - это количество голосов избирателей,
которое необходимо набрать для получения одного места в парламенте).
Далее каждая партия получает столько мест в парламенте,
чему равна целая часть от деления числа голосов за данную партию на первое избирательное частное.
Если после первого раунда распределения мест сумма количества мест, отданных партиям, меньше 450,
то оставшиеся места передаются по одному партиям,
в порядке убывания дробной части частного от деления числа голосов за данную партию на первое избирательное частное.
Если же для двух партий эти дробные части равны,
то преимущество отдается той партии, которая получила большее число голосов.

Формат ввода:
    На вход программе подается список партий, участвовавших в выборах.
    Каждая строка входного файла содержит название партии (строка, возможно, содержащая пробелы),
    затем, через пробел, количество голосов, полученных данной партией – число, не превосходящее 10⁸.

Формат вывода:
    Программа должна вывести названия всех партий и количество голосов в парламенте, полученных данной партией.
    Названия необходимо выводить в том же порядке, в котором они шли во входных данных.
"""

from math import ceil

doc = open('ViboriDeputatovGosDumi.txt', 'r', encoding='utf8')
docOut = open('ViboriDeputatovGosDumi_Out.txt', 'w', encoding='utf8')
x = {}
k = 0
for i in doc:
    a = i.split()
    name = ''
    for j in range(len(a) - 1):
        name += a[j] + ' '
    name = name[:-1]
    x[name] = x.get(name, 0) + int(a[-1])
    k += int(a[-1])
FirstIzbChastnoe = k / 450
z = []
a = 450
for key in x:
    v = x.get(key) / FirstIzbChastnoe
    z.append([key, v])
    a -= int(x.get(key) / FirstIzbChastnoe)
z.sort(key=lambda p: -(p[1] - int(p[1])))
for i in range(a):
    if z[i][1] - int(z[i][1]) == z[i + 1][1] - int(z[i + 1][1]) and i != a - 1:
        if x[z[i][0]] > x[z[i + 1][0]]:
            z[i][1] = ceil(z[i][1])
        else:
            z[i + 1][1] = ceil(z[i + 1][1])
    z[i][1] = ceil(z[i][1])
for i in range(a, len(z)):
    z[i][1] = int(z[i][1])
for i in range(len(z)):
    x[z[i][0]] = z[i][1]
for key in x:
    print(key, x[key])
